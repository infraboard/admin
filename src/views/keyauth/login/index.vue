<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" autocomplete="on" label-position="left">
      <div class="title-container">
        <h3 class="title">
          {{ $t('login.title') }}
        </h3>
        <lang-select class="set-language" />
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          :placeholder="$t('login.username')"
          name="username"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>

      <el-tooltip v-model="capsTooltip" content="Caps lock is On" placement="right" manual>
        <el-form-item prop="password">
          <span class="svg-container">
            <svg-icon icon-class="password" />
          </span>
          <el-input
            :key="passwordType"
            ref="password"
            v-model="loginForm.password"
            :type="passwordType"
            :placeholder="$t('login.password')"
            name="password"
            tabindex="2"
            autocomplete="on"
            @keyup.native="checkCapslock"
            @blur="capsTooltip = false"
            @keyup.enter.native="handleLogin"
          />
          <span class="show-pwd" @click="showPwd">
            <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
          </span>
        </el-form-item>
      </el-tooltip>

      <el-button :loading="loading" size="medium" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">
        {{ $t('login.logIn') }}
      </el-button>

      <div style="position:relative">
        <div class="tips">
          <span>{{ $t('login.thirdparty') }}</span>
        </div>

        <el-button class="fr" type="primary" @click="showDialog=true">
          {{ $t('login.ldap') }}
        </el-button>
      </div>
    </el-form>

    <el-dialog :title="$t('login.thirdparty')" :visible.sync="showDialog">
      {{ $t('login.thirdpartyTips') }}
      <br>
      <br>
      <br>
    </el-dialog>

    <!-- 验证码 -->
    <verify-code :visible.sync="dialogVerifyCodeVisible" :username="loginForm.username" :password="loginForm.password" @change="updateVerifyCode" />
  </div>
</template>

<script>
import Cookies from 'js-cookie'
import LangSelect from '@/components/LangSelect'
import VerifyCode from './components/VerifyCode'

const Base64 = require('js-base64').Base64

export default {
  name: 'Login',
  components: { LangSelect, VerifyCode },
  data() {
    const validateUsername = (rule, value, callback) => {
      callback()
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      dialogVerifyCodeVisible: false,
      loginForm: {
        grant_type: 'password',
        verify_code: '',
        username: '',
        password: ''
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      showDialog: false,
      redirect: undefined,
      otherQuery: {}
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  created() {
    // window.addEventListener('storage', this.afterQRScan)
  },
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  destroyed() {
    // window.removeEventListener('storage', this.afterQRScan)
  },
  methods: {
    updateVerifyCode(code) {
      this.loginForm.verify_code = code
      this.handleLogin()
    },
    checkCapslock(e) {
      const { key } = e
      this.capsTooltip = key && key.length === 1 && (key >= 'A' && key <= 'Z')
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(async valid => {
        if (valid) {
          this.loading = true
          try {
            await this.$store.dispatch('user/login', this.loginForm)

            // get user info
            // note: roles must be a object array! such as: ['admin'] or ,['developer','editor']
            const user = await this.$store.dispatch('user/getInfo')
            console.log(user)

            // generate accessible routes map based on roles
            const accessRoutes = await this.$store.dispatch('permission/generateRoutes', user.roles)
            console.log(accessRoutes)
            // dynamically add accessible routes
            this.$router.addRoutes(accessRoutes)
          } catch (err) {
            if (err.code === 50018) {
              this.dialogVerifyCodeVisible = true
            }
            return
          } finally {
            this.loading = false
          }

          if (!this.$store.getters.isInitialized) {
            // 如果没有初始化 调转初始化页面进行设置
            this.$router.push({ name: 'SubAccountInit' })
          } else if (this.$store.getters.needReset) {
            // 敏感信息通过Cookie传递给重置页面, 过期时间5秒
            Cookies.set('password', Base64.encode(this.loginForm.password.trim(' ')), { expires: 5000 })
            this.$router.push({ path: '/password-reset' })
          } else {
            // 其他直接调转
            this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
          }
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }
  }
}
</script>

